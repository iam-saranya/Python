import math
print(math.factorial(5))

#This line imports the math module in Python. The math module provides mathematical functions, and one of them is factorial.

#math.factorial(5): This line calls the factorial function from the math module with the argument 5. The factorial of a non-negative integer is the product of all positive integers less than or equal to that number. So, 5! (read as "5 factorial") is calculated as 5 * 4 * 3 * 2 * 1, which equals 120.

#print(...): This line prints the result of the math.factorial(5) calculation. In this case, it will print the value 120.
